<template>
    <div class="row" :style="rowStyle">
        <slot></slot>
    </div>
</template>

<script>
export default {
  name: "VioRow",
  props: {
    gutter: {
      type: [Number, String]
    }
  },
  data() {
    return {}
  },
  computed: {
    rowStyle() {
      return {
        marginLeft: -this.gutter / 2 + "px",
        marginRight: -this.gutter / 2 + "px"
      }
    }
  },
  components: {},
  mounted() {
    this.$children.forEach(vm => {
      vm.gutter = this.gutter
    })
  }
}
</script>

<style scoped lang="scss">
.row {
  display: flex;
}
</style>
